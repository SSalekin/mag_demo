#!/usr/bin/env python3
"""BMAD-style coding team with optional Titan memory and optional LLM generation.

This is the coding-agent orchestration prototype for the Titan memory
project. Its default path remains deterministic so the basic software-factory
workflow can be tested locally, while Step 6 can optionally delegate code
generation to an Agno/Ollama Dev Agent:

Project Manager -> Business -> Dev -> DevOps -> QA -> Evaluator

Step 3 added a controlled Titan connection in read-only mode. The Project
Manager can retrieve relevant long-term memory before the workflow starts and
share that context with the deterministic BMAD roles.

Step 4 adds a controlled memory-validation gate. The workflow still does not
write to Titan by default. It proposes a memory candidate, the Evaluator and
Project Manager validate whether it is eligible for long-term storage, and Titan
is written only when the caller explicitly enables ``store_approved_memory``.

The team writes generated files to ``Test_models/staging`` through the coding
tools added in step 1, validates them, and publishes accepted output to
``Test_models/workspace``.

Step 7 adds an automatic repair loop: when QA fails, the Dev Agent receives the
QA failures, rewrites the staged files, and QA runs again before the Evaluator
rejects or approves the result.

Step 8 strengthens open-ended code generation: the LLM prompt now explicitly
forbids placeholder skeletons, LLM output passes a quality gate before staging,
and the deterministic fallback includes a quadratic-equation solver template.

Step 9 adds runtime behavior validation and a stricter publish filter: QA can
execute generated CLIs with expected outputs, while workspace/ receives only
important user-facing files instead of validation-only artifacts.

Step 11 improves Docker/sandbox execution: when requested, QA builds a staging
Docker image, runs generated unit tests and behavior checks inside the container,
and falls back cleanly to local validation when Docker is unavailable.
"""

