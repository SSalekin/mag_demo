class QAAgent:
    name = "QA"
    role = "validation"

    def validate(self, spec: TaskSpec | None = None, run_docker: bool = False, attempt: int = 0) -> tuple[QAReport, BMADStep]:
        checks: list[str] = []
        failures: list[str] = []
        required_files = ["app.py", "README.md", "Dockerfile", "docker-compose.yml"]

        for relative_path in required_files:
            target = STAGING_DIR / relative_path
            if target.exists() and target.is_file():
                checks.append(f"Found staging/{relative_path}.")
            else:
                failures.append(f"Missing staging/{relative_path}.")

        app_path = STAGING_DIR / "app.py"
        if app_path.exists():
            try:
                app_code = app_path.read_text(encoding="utf-8")
                ast.parse(app_code)
                checks.append("app.py syntax validation passed.")
                if spec is not None and _app_looks_like_generic_placeholder(app_code):
                    if spec.project_kind != "generic" or _request_requires_specific_implementation(spec.original_request):
                        failures.append(
                            "Generated app.py is a generic placeholder for a request that needs task-specific behavior."
                        )
            except SyntaxError as exc:
                failures.append(f"app.py syntax error: {exc}")

        test_path = STAGING_DIR / "test_app.py"
        if test_path.exists():
            test_syntax_ok = False
            try:
                ast.parse(test_path.read_text(encoding="utf-8"))
                checks.append("test_app.py syntax validation passed.")
                test_syntax_ok = True
            except SyntaxError as exc:
                failures.append(f"test_app.py syntax error: {exc}")

            if test_syntax_ok:
                try:
                    completed = subprocess.run(
                        [sys.executable, "-m", "unittest", "test_app.py"],
                        cwd=STAGING_DIR,
                        text=True,
                        capture_output=True,
                        timeout=20,
                    )
                    checks.append("Generated unit tests executed locally.")
                    if completed.returncode == 0:
                        checks.append("Generated unit tests passed locally.")
                    else:
                        failures.append(
                            "Generated unit tests failed locally: "
                            + (completed.stdout + completed.stderr).strip()[:800]
                        )
                except Exception as exc:
                    failures.append(f"Generated unit tests could not be executed locally: {exc}")

        if spec is not None and spec.behavior_checks:
            for behavior in spec.behavior_checks:
                completed = None
                try:
                    for setup_dir in behavior.setup_dirs:
                        (STAGING_DIR / setup_dir).mkdir(parents=True, exist_ok=True)
                    for relative_path, content in behavior.setup_files.items():
                        setup_path = STAGING_DIR / relative_path
                        setup_path.parent.mkdir(parents=True, exist_ok=True)
                        setup_path.write_text(content, encoding="utf-8")

                    completed = subprocess.run(
                        [sys.executable, "app.py", *behavior.args],
                        cwd=STAGING_DIR,
                        text=True,
                        capture_output=True,
                        timeout=20,
                    )
                    combined_output = (completed.stdout + completed.stderr).strip()
                    if completed.returncode != behavior.expected_returncode:
                        failures.append(
                            f"Behavior check '{behavior.name}' returned {completed.returncode}, "
                            f"expected {behavior.expected_returncode}: {combined_output[:500]}"
                        )
                        continue
                    missing = [fragment for fragment in behavior.expected_output_fragments if fragment not in combined_output]
                    if missing:
                        failures.append(
                            f"Behavior check '{behavior.name}' output missing {missing}: {combined_output[:500]}"
                        )
                    else:
                        checks.append(f"Behavior check '{behavior.name}' passed.")
                except Exception as exc:
                    failures.append(f"Behavior check '{behavior.name}' could not run: {exc}")
                finally:
                    for cleanup in behavior.cleanup_files:
                        cleanup_path = STAGING_DIR / cleanup
                        if cleanup_path.exists() and cleanup_path.is_file():
                            cleanup_path.unlink()
                    for cleanup in behavior.cleanup_paths:
                        cleanup_path = STAGING_DIR / cleanup
                        if cleanup_path.exists():
                            if cleanup_path.is_dir():
                                shutil.rmtree(cleanup_path)
                            else:
                                cleanup_path.unlink()
        elif spec is not None:
            checks.append("No runtime behavior checks defined for this app type.")

        docker_output: str | None = None
        if run_docker:
            docker_commands: list[tuple[str, list[str]]] = []
            docker_expectations: dict[str, tuple[int, list[str]]] = {}

            if test_path.exists():
                docker_commands.append(("docker_unit_tests", ["python", "-m", "unittest", "test_app.py"]))
                docker_expectations["docker_unit_tests"] = (0, [])
            else:
                docker_commands.append(("docker_import_app", ["python", "-c", "import app; print('app import ok')"]))
                docker_expectations["docker_import_app"] = (0, ["app import ok"])

            if spec is not None and spec.behavior_checks:
                try:
                    for behavior in spec.behavior_checks:
                        for setup_dir in behavior.setup_dirs:
                            (STAGING_DIR / setup_dir).mkdir(parents=True, exist_ok=True)
                        for relative_path, content in behavior.setup_files.items():
                            setup_path = STAGING_DIR / relative_path
                            setup_path.parent.mkdir(parents=True, exist_ok=True)
                            setup_path.write_text(content, encoding="utf-8")
                        docker_commands.append((behavior.name, ["python", "app.py", *behavior.args]))
                        docker_expectations[behavior.name] = (
                            behavior.expected_returncode,
                            list(behavior.expected_output_fragments),
                        )

                    docker_result = run_commands_in_docker(docker_commands)
                    docker_output = docker_result.to_text()
                    if docker_result.skipped:
                        checks.append(f"Docker execution skipped: {docker_result.reason} Local QA fallback already ran.")
                    elif not docker_result.success:
                        checks.append("Docker execution attempted in isolated sandbox.")
                        if docker_result.build_returncode not in (None, 0):
                            failures.append("Docker image build failed: " + docker_result.reason)
                        for command_result in docker_result.commands:
                            expected_code, expected_fragments = docker_expectations.get(command_result.name, (0, []))
                            combined_output = command_result.output
                            if command_result.returncode != expected_code:
                                failures.append(
                                    f"Docker check '{command_result.name}' returned {command_result.returncode}, "
                                    f"expected {expected_code}: {combined_output[:500]}"
                                )
                                continue
                            missing = [fragment for fragment in expected_fragments if fragment not in combined_output]
                            if missing:
                                failures.append(
                                    f"Docker check '{command_result.name}' output missing {missing}: {combined_output[:500]}"
                                )
                            else:
                                checks.append(f"Docker check '{command_result.name}' passed.")
                    else:
                        checks.append("Docker execution attempted in isolated sandbox.")
                        checks.append("Docker image build passed.")
                        for command_result in docker_result.commands:
                            expected_code, expected_fragments = docker_expectations.get(command_result.name, (0, []))
                            combined_output = command_result.output
                            missing = [fragment for fragment in expected_fragments if fragment not in combined_output]
                            if command_result.returncode == expected_code and not missing:
                                checks.append(f"Docker check '{command_result.name}' passed.")
                            else:
                                failures.append(
                                    f"Docker check '{command_result.name}' failed: returncode={command_result.returncode}, "
                                    f"expected={expected_code}, missing={missing}, output={combined_output[:500]}"
                                )
                finally:
                    if spec is not None:
                        for behavior in spec.behavior_checks:
                            for cleanup in behavior.cleanup_files:
                                cleanup_path = STAGING_DIR / cleanup
                                if cleanup_path.exists() and cleanup_path.is_file():
                                    cleanup_path.unlink()
                            for cleanup in behavior.cleanup_paths:
                                cleanup_path = STAGING_DIR / cleanup
                                if cleanup_path.exists():
                                    if cleanup_path.is_dir():
                                        shutil.rmtree(cleanup_path)
                                    else:
                                        cleanup_path.unlink()
        else:
            checks.append("Docker execution skipped for this minimal local test.")

        report = QAReport(passed=not failures, checks=checks, failures=failures, docker_output=docker_output, attempt=attempt)
        summary_prefix = f"QA attempt {attempt}" if attempt else "QA"
        step = BMADStep(
            agent=self.name,
            role=self.role,
            summary=(f"{summary_prefix} passed." if report.passed else f"{summary_prefix} failed."),
            actions=["check_required_files", "syntax_validate", "run_generated_unit_tests", "run_behavior_checks"] + (["run_docker"] if run_docker else ["skip_docker"]),
            status="passed" if report.passed else "failed",
        )
        return report, step


class EvaluatorAgent:
    name = "Evaluator"
    role = "quality gate"

    def evaluate(self, spec: TaskSpec, qa_report: QAReport) -> BMADStep:
        if qa_report.passed:
            summary = "Accepted: generated artifacts satisfy the minimal BMAD acceptance criteria."
            status = "approved"
            actions = ["approve_publish"]
        else:
            summary = "Rejected: QA found blocking issues."
            status = "rejected"
            actions = ["request_revision"]
        return BMADStep(agent=self.name, role=self.role, summary=summary, actions=actions, status=status)

    def validate_memory_candidate(
        self,
        *,
        candidate: str | None,
        approved: bool,
        published: bool,
        qa_report: QAReport,
        store_approved_memory: bool,
    ) -> MemoryValidationDecision:
        """Apply the controlled long-term memory write policy.

        The policy is deliberately conservative: only approved, published and
        QA-passing outputs may be stored, and even then only when the caller
        explicitly enables the write gate.
        """

        if not candidate:
            return MemoryValidationDecision(
                candidate=candidate,
                should_store=False,
                stored=False,
                reason="No memory candidate was produced.",
                store_result="skipped_no_candidate",
            )
        if not store_approved_memory:
            return MemoryValidationDecision(
                candidate=candidate,
                should_store=False,
                stored=False,
                reason="Memory write disabled; candidate proposed only.",
                approved_by=[self.name, "Project Manager"],
                store_result="skipped_write_disabled",
            )
        if not approved or not published or not qa_report.passed:
            return MemoryValidationDecision(
                candidate=candidate,
                should_store=False,
                stored=False,
                reason="Memory candidate rejected because the workflow was not fully approved, published and QA-passing.",
                approved_by=[self.name, "Project Manager"],
                store_result="skipped_not_validated",
            )
        return MemoryValidationDecision(
            candidate=candidate,
            should_store=True,
            stored=False,
            reason="Memory candidate approved for storage by Evaluator and Project Manager.",
            approved_by=[self.name, "Project Manager"],
            store_result="approved_pending_store",
        )


