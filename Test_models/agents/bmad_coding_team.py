#!/usr/bin/env python3
"""Minimal BMAD-style coding team with optional Titan read-only memory.

This is the first coding-agent orchestration prototype for the Titan memory
project. It deliberately avoids Agno and Ollama so the basic software-factory
workflow can be tested deterministically:

Project Manager -> Business -> Dev -> DevOps -> QA -> Evaluator

Step 3 added a controlled Titan connection in read-only mode. The Project
Manager can retrieve relevant long-term memory before the workflow starts and
share that context with the deterministic BMAD roles.

Step 4 adds a controlled memory-validation gate. The workflow still does not
write to Titan by default. It proposes a memory candidate, the Evaluator and
Project Manager validate whether it is eligible for long-term storage, and Titan
is written only when the caller explicitly enables ``store_approved_memory``.

The team writes generated files to ``Test_models/staging`` through the coding
tools added in step 1, validates them, and publishes accepted output to
``Test_models/workspace``.
"""

