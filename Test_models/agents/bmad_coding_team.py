class ReadOnlyMemory(Protocol):
    """Small protocol for read-only memory dependencies.

    TitanAgentMemory satisfies this protocol, and tests can inject a fake
    object with the same read-only methods. Write methods are intentionally not
    part of the protocol.
    """

    def load(self, path: str | Path | None = None) -> bool: ...

    def recall(self, query: str, top_k: int | None = None, min_score: float | None = None) -> list[Any]: ...


# ---------------------------------------------------------------------------
# Data structures
# ---------------------------------------------------------------------------
@dataclass(frozen=True)
class MemoryContext:
    """Read-only memory context recovered before the BMAD workflow starts."""

    enabled: bool
    query: str
    context: str
    records: list[dict[str, Any]] = field(default_factory=list)
    loaded: bool = False
    source: str = "disabled"
    error: str | None = None

    @property
    def has_relevant_memories(self) -> bool:
        return bool(self.records)


@dataclass(frozen=True)
class BMADArtifact:
    """A file generated by the Dev, Designer or DevOps step."""

    path: str
    purpose: str
    producer: str


@dataclass(frozen=True)
class BMADStep:
    """A deterministic role response in the BMAD workflow."""

    agent: str
    role: str
    summary: str
    actions: list[str] = field(default_factory=list)
    artifacts: list[BMADArtifact] = field(default_factory=list)
    status: str = "done"


@dataclass(frozen=True)
class TaskSpec:
    """Small implementation specification produced by the Business Agent."""

    original_request: str
    project_name: str
    goal: str
    requirements: list[str]
    acceptance_criteria: list[str]
    target_language: str = "python"
    entrypoint: str = "app.py"
    memory_context: str = ""
    memory_context_used: bool = False


@dataclass(frozen=True)
class QAReport:
    """QA result before the Evaluator decides whether to publish."""

    passed: bool
    checks: list[str]
    failures: list[str]
    docker_output: str | None = None


@dataclass(frozen=True)
class BMADResult:
    """Full result returned by the BMAD coding team."""

    task: str
    approved: bool
    published: bool
    publish_result: str
    workspace_files: list[str]
    staging_files_after_run: list[str]
    steps: list[BMADStep]
    qa_report: QAReport
    memory_context: MemoryContext
    memory_candidate: str | None
    final_message: str

    def to_dict(self) -> dict[str, Any]:
        return asdict(self)


