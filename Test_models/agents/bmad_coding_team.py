class BMADCodingTeam:
    """Deterministic BMAD coding workflow using staging/workspace tools.

    Titan memory is optional. By default it is read-only: only ``load`` and
    ``recall`` are used. Step 4 can store a validated memory candidate only when
    the caller explicitly enables ``store_approved_memory``.
    """

    def __init__(
        self,
        *,
        memory: ReadOnlyMemory | None = None,
        use_titan_memory: bool = False,
        memory_path: str | Path | None = None,
        memory_top_k: int = 5,
        memory_min_score: float = 0.12,
        store_approved_memory: bool = False,
    ) -> None:
        self.project_manager = ProjectManagerAgent()
        self.business = BusinessAgent()
        self.dev = DevAgent()
        self.designer = DesignerAgent()
        self.devops = DevOpsAgent()
        self.qa = QAAgent()
        self.evaluator = EvaluatorAgent()
        self.memory = memory
        self._memory_injected = memory is not None
        self.store_approved_memory = bool(store_approved_memory)
        self.use_titan_memory = bool(use_titan_memory or memory is not None or self.store_approved_memory)
        self.memory_path = Path(memory_path) if memory_path is not None else Path("agent_titan_memory.pt")
        self.memory_top_k = int(memory_top_k)
        self.memory_min_score = float(memory_min_score)

    def run(self, task: str, *, run_docker: bool = False, clean_workspace: bool = False) -> BMADResult:
        """Run the complete minimal coding workflow.

        Args:
            task: User coding request.
            run_docker: If true, QA calls Docker through ``run_tests_in_docker``.
                The default is false so this benchmark works on machines where
                Docker is not installed or not running.
            clean_workspace: If true, workspace/ is cleared before publishing.
        """

        task = (task or "").strip()
        if not task:
            raise ValueError("A non-empty coding task is required.")

        memory_context = self._retrieve_memory_context(task)

        steps: list[BMADStep] = []
        steps.append(self.project_manager.start(task, clean_workspace=clean_workspace, memory_context=memory_context))

        spec, business_step = self.business.define_spec(task, memory_context=memory_context)
        steps.append(business_step)
        steps.append(self.dev.implement(spec))
        steps.append(self.designer.review(spec))
        steps.append(self.devops.prepare_environment())

        qa_report, qa_step = self.qa.validate(run_docker=run_docker)
        steps.append(qa_step)

        evaluation_step = self.evaluator.evaluate(spec, qa_report)
        steps.append(evaluation_step)

        approved = evaluation_step.status == "approved"
        if approved:
            publish_result = publish_to_workspace(useful_files=["app.py", "README.md", "test_app.py"] if (STAGING_DIR / "test_app.py").exists() else ["app.py", "README.md"])
            published = publish_result.startswith("Success:")
        else:
            publish_result = "Not published because QA/Evaluator rejected the result."
            published = False

        memory_candidate = self._build_memory_candidate(
            task=task,
            approved=approved,
            published=published,
            workspace_files=_workspace_file_list(),
            qa_report=qa_report,
        )
        memory_validation = self.evaluator.validate_memory_candidate(
            candidate=memory_candidate,
            approved=approved,
            published=published,
            qa_report=qa_report,
            store_approved_memory=self.store_approved_memory,
        )
        if memory_validation.should_store:
            memory_validation = self._store_validated_memory(memory_validation)

        # Build a temporary result to let the PM create the final decision step.
        partial_result = BMADResult(
            task=task,
            approved=approved,
            published=published,
            publish_result=publish_result,
            workspace_files=_workspace_file_list(),
            staging_files_after_run=_staging_file_list(),
            steps=steps,
            qa_report=qa_report,
            memory_context=memory_context,
            memory_candidate=memory_candidate,
            memory_validation=memory_validation,
            final_message="",
        )
        steps.append(self.project_manager.finish(partial_result))

        workspace_files = _workspace_file_list()
        staging_files = _staging_file_list()
        final_message = self._build_final_message(
            task,
            approved,
            published,
            publish_result,
            workspace_files,
            qa_report,
            memory_context,
            memory_candidate,
            memory_validation,
        )

        return BMADResult(
            task=task,
            approved=approved,
            published=published,
            publish_result=publish_result,
            workspace_files=workspace_files,
            staging_files_after_run=staging_files,
            steps=steps,
            qa_report=qa_report,
            memory_context=memory_context,
            memory_candidate=memory_candidate,
            memory_validation=memory_validation,
            final_message=final_message,
        )

    def _get_memory(self) -> ReadOnlyMemory:
        """Return the configured read-only memory object, creating Titan lazily."""

        if self.memory is not None:
            return self.memory

        # Lazy import keeps the default no-memory workflow light and avoids
        # importing PyTorch/Titan unless the caller explicitly asks for memory.
        from agents.titan_agent_memory import TitanAgentMemory

        self.memory = TitanAgentMemory(
            memory_path=self.memory_path,
            top_k=self.memory_top_k,
            min_score=self.memory_min_score,
        )
        return self.memory

    def _retrieve_memory_context(self, task: str) -> MemoryContext:
        """Retrieve Titan memory context without writing to long-term memory."""

        if not self.use_titan_memory:
            return MemoryContext(
                enabled=False,
                query=task,
                context="Titan memory read-only retrieval is disabled for this run.",
                source="disabled",
            )

        try:
            memory = self._get_memory()
            loaded = False
            if hasattr(memory, "load"):
                loaded = bool(memory.load())

            raw_records = memory.recall(task, top_k=self.memory_top_k, min_score=self.memory_min_score)
            records = [self._memory_record_to_dict(record) for record in raw_records]
            context = self._format_memory_context(records)
            source = "injected_memory" if self._memory_injected else str(self.memory_path)
            return MemoryContext(
                enabled=True,
                query=task,
                context=context,
                records=records,
                loaded=loaded,
                source=source,
            )
        except Exception as exc:  # pragma: no cover - defensive path
            return MemoryContext(
                enabled=True,
                query=task,
                context="Titan memory retrieval failed safely. Continuing without memory context.",
                records=[],
                loaded=False,
                source=str(self.memory_path),
                error=str(exc),
            )

    @staticmethod
    def _memory_record_to_dict(record: Any) -> dict[str, Any]:
        if isinstance(record, dict):
            return dict(record)
        data: dict[str, Any] = {}
        for key in ["id", "text", "score", "subject", "property", "active", "metadata"]:
            if hasattr(record, key):
                data[key] = getattr(record, key)
        if not data:
            data["text"] = str(record)
        if "text" in data:
            data["text"] = str(data["text"])
        if "score" in data:
            try:
                data["score"] = float(data["score"])
            except (TypeError, ValueError):
                data["score"] = 0.0
        return data

    @staticmethod
    def _format_memory_context(records: Sequence[dict[str, Any]]) -> str:
        if not records:
            return "No relevant long-term Titan memory found."
        lines = ["Relevant long-term Titan memories (read-only):"]
        for idx, record in enumerate(records, start=1):
            score = record.get("score", 1.0)
            subject = record.get("subject") or "?"
            prop = record.get("property") or "?"
            text = record.get("text", "")
            try:
                score_text = f"{float(score):.3f}"
            except (TypeError, ValueError):
                score_text = "?"
            lines.append(f"{idx}. [score={score_text}; subject={subject}; property={prop}] {text}")
        return "\n".join(lines)

    @staticmethod
    def _build_memory_candidate(
        *,
        task: str,
        approved: bool,
        published: bool,
        workspace_files: Sequence[str],
        qa_report: QAReport,
    ) -> str:
        status = "approved" if approved else "rejected"
        files = ", ".join(workspace_files) if workspace_files else "none"
        qa_status = "passed" if qa_report.passed else "failed"
        return (
            "Candidate memory only; do not store automatically. "
            f"BMAD coding workflow {status} task={task!r}; "
            f"published={published}; workspace_files={files}; QA={qa_status}."
        )

    def _store_validated_memory(self, decision: MemoryValidationDecision) -> MemoryValidationDecision:
        """Store an approved memory candidate through the controlled write gate."""

        if not decision.should_store or not decision.candidate:
            return decision
        try:
            memory = self._get_memory()
            if not hasattr(memory, "store"):
                return MemoryValidationDecision(
                    candidate=decision.candidate,
                    should_store=True,
                    stored=False,
                    reason=decision.reason,
                    approved_by=decision.approved_by,
                    records_stored=0,
                    store_result="failed_missing_store_method",
                    error="Configured memory object does not expose store().",
                )
            records = memory.store(
                decision.candidate,
                metadata={
                    "source": "bmad_coding_team",
                    "validation": "project_manager_evaluator_gate",
                    "store_policy": "explicit_store_approved_memory",
                },
            )
            try:
                records_stored = len(records)
            except TypeError:
                records_stored = 1
            if hasattr(memory, "save"):
                memory.save()
            return MemoryValidationDecision(
                candidate=decision.candidate,
                should_store=True,
                stored=True,
                reason=decision.reason,
                approved_by=decision.approved_by,
                records_stored=records_stored,
                store_result="stored_and_saved",
            )
        except Exception as exc:  # pragma: no cover - defensive path
            return MemoryValidationDecision(
                candidate=decision.candidate,
                should_store=True,
                stored=False,
                reason=decision.reason,
                approved_by=decision.approved_by,
                records_stored=0,
                store_result="failed_exception",
                error=str(exc),
            )

    def _build_final_message(
        self,
        task: str,
        approved: bool,
        published: bool,
        publish_result: str,
        workspace_files: Sequence[str],
        qa_report: QAReport,
        memory_context: MemoryContext,
        memory_candidate: str | None,
        memory_validation: MemoryValidationDecision,
    ) -> str:
        status = "APPROVED" if approved else "REJECTED"
        lines = [
            f"BMAD coding workflow result: {status}",
            f"Task: {task}",
            f"Published: {published}",
            f"Publish result: {publish_result}",
            "Workspace files: " + (", ".join(workspace_files) if workspace_files else "none"),
            (
                f"Titan memory read-only: enabled, records={len(memory_context.records)}, source={memory_context.source}"
                if memory_context.enabled
                else "Titan memory read-only: disabled"
            ),
            "QA checks:",
        ]
        lines.extend(f"- {check}" for check in qa_report.checks)
        if qa_report.failures:
            lines.append("QA failures:")
            lines.extend(f"- {failure}" for failure in qa_report.failures)
        if memory_candidate:
            lines.append("Memory candidate (not stored automatically):")
            lines.append(memory_candidate)
        lines.append("Memory validation:")
        lines.append(f"- should_store: {memory_validation.should_store}")
        lines.append(f"- stored: {memory_validation.stored}")
        lines.append(f"- reason: {memory_validation.reason}")
        lines.append(f"- store_result: {memory_validation.store_result}")
        if memory_validation.error:
            lines.append(f"- error: {memory_validation.error}")
        return "\n".join(lines)


def run_bmad_task(
    task: str,
    *,
    run_docker: bool = False,
    clean_workspace: bool = False,
    use_titan_memory: bool = False,
    memory_path: str | Path | None = None,
    memory_top_k: int = 5,
    memory_min_score: float = 0.12,
    store_approved_memory: bool = False,
) -> BMADResult:
    """Convenience function for scripts and future agent integrations."""

    return BMADCodingTeam(
        use_titan_memory=use_titan_memory,
        memory_path=memory_path,
        memory_top_k=memory_top_k,
        memory_min_score=memory_min_score,
        store_approved_memory=store_approved_memory,
    ).run(task, run_docker=run_docker, clean_workspace=clean_workspace)


def main(argv: Sequence[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description="Run the minimal BMAD coding team.")
    parser.add_argument("--task", required=True, help="Coding task to execute.")
    parser.add_argument("--run-docker", action="store_true", help="Run Docker validation during QA.")
    parser.add_argument("--clean-workspace", action="store_true", help="Clear workspace before publishing.")
    parser.add_argument("--use-titan-memory", action="store_true", help="Retrieve Titan memory in read-only mode before BMAD starts.")
    parser.add_argument("--memory-path", default="agent_titan_memory.pt", help="Titan memory file to read when --use-titan-memory is enabled.")
    parser.add_argument("--memory-top-k", type=int, default=5, help="Number of Titan memories to retrieve.")
    parser.add_argument("--memory-min-score", type=float, default=0.12, help="Minimum Titan retrieval score.")
    parser.add_argument("--store-approved-memory", action="store_true", help="Store the validated BMAD memory candidate in Titan. Disabled by default.")
    parser.add_argument("--json", action="store_true", help="Print the full result as JSON.")
    args = parser.parse_args(argv)

    result = run_bmad_task(
        args.task,
        run_docker=args.run_docker,
        clean_workspace=args.clean_workspace,
        use_titan_memory=args.use_titan_memory,
        memory_path=args.memory_path,
        memory_top_k=args.memory_top_k,
        memory_min_score=args.memory_min_score,
        store_approved_memory=args.store_approved_memory,
    )
    if args.json:
        print(json.dumps(result.to_dict(), ensure_ascii=False, indent=2))
    else:
        print(result.final_message)
    return 0 if result.approved else 1


