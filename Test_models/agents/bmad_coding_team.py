@dataclass(frozen=True)
class MemoryContext:
    """Read-only memory context recovered before the BMAD workflow starts."""

    enabled: bool
    query: str
    context: str
    records: list[dict[str, Any]] = field(default_factory=list)
    loaded: bool = False
    source: str = "disabled"
    error: str | None = None

    @property
    def has_relevant_memories(self) -> bool:
        return bool(self.records)


@dataclass(frozen=True)
class MemoryValidationDecision:
    """Decision produced by the controlled memory gate.

    The candidate is validated by the Evaluator/Project Manager pair. Storage is
    allowed only when the generated result is approved and the caller explicitly
    enables the write gate. This keeps long-term Titan memory clean by default.
    """

    candidate: str | None
    should_store: bool
    stored: bool
    reason: str
    approved_by: list[str] = field(default_factory=list)
    records_stored: int = 0
    store_result: str = "not_requested"
    error: str | None = None


@dataclass(frozen=True)
class BMADArtifact:
    """A file generated by the Dev, Designer or DevOps step."""

    path: str
    purpose: str
    producer: str


@dataclass(frozen=True)
class BMADStep:
    """A deterministic role response in the BMAD workflow."""

    agent: str
    role: str
    summary: str
    actions: list[str] = field(default_factory=list)
    artifacts: list[BMADArtifact] = field(default_factory=list)
    status: str = "done"


@dataclass(frozen=True)
class TaskSpec:
    """Small implementation specification produced by the Business Agent."""

    original_request: str
    project_name: str
    goal: str
    requirements: list[str]
    acceptance_criteria: list[str]
    target_language: str = "python"
    entrypoint: str = "app.py"
    project_kind: str = "generic"
    cli_examples: list[str] = field(default_factory=list)
    test_command: str = "python -m unittest test_app.py"
    memory_context: str = ""
    memory_context_used: bool = False


@dataclass(frozen=True)
class QAReport:
    """QA result before the Evaluator decides whether to publish."""

    passed: bool
    checks: list[str]
    failures: list[str]
    docker_output: str | None = None


@dataclass(frozen=True)
class BMADResult:
    """Full result returned by the BMAD coding team."""

    task: str
    approved: bool
    published: bool
    publish_result: str
    workspace_files: list[str]
    staging_files_after_run: list[str]
    steps: list[BMADStep]
    qa_report: QAReport
    memory_context: MemoryContext
    memory_candidate: str | None
    memory_validation: MemoryValidationDecision
    final_message: str

    def to_dict(self) -> dict[str, Any]:
        return asdict(self)


