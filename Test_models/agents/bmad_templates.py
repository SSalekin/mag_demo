def _memory_store_app(spec: Any) -> str:
    request = _json(_request(spec))
    return f'''"""Small user-memory store CLI generated by BMAD."""

from __future__ import annotations

import argparse
import json
from pathlib import Path

REQUEST = {request}
DEFAULT_MEMORY_FILE = Path("memories.json")


def load_memories(path: Path = DEFAULT_MEMORY_FILE) -> dict[str, list[str]]:
    if not path.exists():
        return {{}}
    data = json.loads(path.read_text(encoding="utf-8"))
    if not isinstance(data, dict):
        raise ValueError("Memory file must contain a JSON object.")
    return {{str(key): [str(item) for item in value] for key, value in data.items()}}


def save_memories(memories: dict[str, list[str]], path: Path = DEFAULT_MEMORY_FILE) -> None:
    path.write_text(json.dumps(memories, indent=2, ensure_ascii=False), encoding="utf-8")


def store_memory(user: str, memory: str, path: Path = DEFAULT_MEMORY_FILE) -> list[str]:
    user = user.strip()
    memory = memory.strip()
    if not user or not memory:
        raise ValueError("User and memory must not be empty.")
    memories = load_memories(path)
    memories.setdefault(user, []).append(memory)
    save_memories(memories, path)
    return memories[user]


def retrieve_memories(user: str, path: Path = DEFAULT_MEMORY_FILE) -> list[str]:
    return load_memories(path).get(user.strip(), [])


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Store and retrieve small user memories.")
    subparsers = parser.add_subparsers(dest="command", required=True)
    store = subparsers.add_parser("store", help="Store a memory for a user.")
    store.add_argument("user")
    store.add_argument("memory")
    retrieve = subparsers.add_parser("retrieve", help="Retrieve memories for a user.")
    retrieve.add_argument("user")
    return parser


def main(argv: list[str] | None = None) -> int:
    args = build_parser().parse_args(argv)
    if args.command == "store":
        memories = store_memory(args.user, args.memory)
        print(f"Stored memory #{{len(memories)}} for {{args.user}}.")
    elif args.command == "retrieve":
        memories = retrieve_memories(args.user)
        if not memories:
            print(f"No memories found for {{args.user}}.")
        else:
            for index, memory in enumerate(memories, start=1):
                print(f"{{index}}. {{memory}}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
'''


