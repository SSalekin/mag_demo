def _caesar_cipher_app(spec: Any) -> str:
    request = _json(_request(spec))
    return f'''"""Caesar cipher CLI generated by BMAD."""

from __future__ import annotations

import argparse

REQUEST = {request}
ALPHABET_SIZE = 26


def shift_char(char: str, shift: int) -> str:
    if not char.isalpha():
        return char
    base = ord("A") if char.isupper() else ord("a")
    return chr((ord(char) - base + shift) % ALPHABET_SIZE + base)


def caesar_encrypt(message: str, shift: int) -> str:
    return "".join(shift_char(char, shift) for char in message)


def caesar_decrypt(message: str, shift: int) -> str:
    return caesar_encrypt(message, -shift)


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Encrypt or decrypt messages with a Caesar cipher.")
    parser.add_argument("mode", choices=["encrypt", "decrypt"])
    parser.add_argument("message")
    parser.add_argument("shift", type=int)
    return parser


def main(argv: list[str] | None = None) -> int:
    args = build_parser().parse_args(argv)
    if args.mode == "encrypt":
        print(caesar_encrypt(args.message, args.shift))
    else:
        print(caesar_decrypt(args.message, args.shift))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
'''


