def _email_phone_validator_app(spec: Any) -> str:
    request = _json(_request(spec))
    return f'''"""Email and phone validator CLI generated by BMAD."""

from __future__ import annotations

import argparse
import re

REQUEST = {request}
EMAIL_RE = re.compile(r"^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{{2,}}$")
PHONE_RE = re.compile(r"^\\+?[0-9][0-9 .-]{{7,18}}[0-9]$")


def is_valid_email(value: str) -> bool:
    return bool(EMAIL_RE.fullmatch(value.strip()))


def is_valid_phone(value: str) -> bool:
    compact = re.sub(r"[ .-]", "", value.strip())
    return bool(PHONE_RE.fullmatch(value.strip())) and 8 <= len(compact.lstrip("+")) <= 15


def validate(kind: str, value: str) -> bool:
    if kind == "email":
        return is_valid_email(value)
    if kind == "phone":
        return is_valid_phone(value)
    raise ValueError("kind must be 'email' or 'phone'")


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Validate email addresses and phone numbers.")
    parser.add_argument("kind", choices=["email", "phone"])
    parser.add_argument("value")
    return parser


def main(argv: list[str] | None = None) -> int:
    args = build_parser().parse_args(argv)
    ok = validate(args.kind, args.value)
    print("valid" if ok else "invalid")
    return 0 if ok else 1


if __name__ == "__main__":
    raise SystemExit(main())
'''


