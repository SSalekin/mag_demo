#!/usr/bin/env python3
"""Deterministic BMAD code templates for common Python CLI requests.

These templates are intentionally small and standard-library only. They give the
BMAD workflow a strong offline fallback for frequent coding prompts while the
LLM path remains responsible for open-ended requests outside these patterns.
"""

from __future__ import annotations

import json
from typing import Any


def _request(spec: Any) -> str:
    return str(getattr(spec, "original_request", "") or "")


def _json(text: str) -> str:
    return json.dumps(text, ensure_ascii=False)


def build_app_template(project_kind: str, spec: Any) -> str | None:
    builders = {
        "memory_store": _memory_store_app,
        "csv_statistics": _csv_statistics_app,
        "multi_agent_simulation": _multi_agent_app,
        "email_phone_validator": _email_phone_validator_app,
        "file_organizer": _file_organizer_app,
        "caesar_cipher": _caesar_cipher_app,
        "chatbot_with_memory": _chatbot_memory_app,
        "test_runner": _test_runner_app,
        "calculator_cli": _calculator_app,
        "text_analyzer": _text_analyzer_app,
    }
    builder = builders.get(project_kind)
    return builder(spec) if builder else None


