def _csv_statistics_app(spec: Any) -> str:
    request = _json(_request(spec))
    return f'''"""CSV statistics CLI generated by BMAD."""

from __future__ import annotations

import argparse
import csv
from pathlib import Path

REQUEST = {request}


def read_numeric_values(path: Path, column: str | None = None) -> list[float]:
    values: list[float] = []
    with path.open(newline="", encoding="utf-8") as handle:
        reader = csv.DictReader(handle)
        if reader.fieldnames is None:
            raise ValueError("CSV file must have a header row.")
        selected_column = column or next((name for name in reader.fieldnames if name.lower() in {{"score", "value", "amount", "number"}}), None)
        selected_column = selected_column or reader.fieldnames[-1]
        if selected_column not in reader.fieldnames:
            raise ValueError(f"Column {{selected_column!r}} not found in CSV file.")
        for row in reader:
            raw = (row.get(selected_column) or "").strip()
            if raw:
                values.append(float(raw))
    if not values:
        raise ValueError("No numeric values found.")
    return values


def calculate_statistics(values: list[float]) -> dict[str, float]:
    ordered = sorted(values)
    count = len(values)
    return {{
        "count": float(count),
        "min": ordered[0],
        "max": ordered[-1],
        "mean": sum(values) / count,
    }}


def format_statistics(stats: dict[str, float]) -> str:
    return "\\n".join([
        f"count: {{int(stats['count'])}}",
        f"min: {{stats['min']:.2f}}",
        f"max: {{stats['max']:.2f}}",
        f"mean: {{stats['mean']:.2f}}",
    ])


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Read a CSV file and calculate statistics for one numeric column.")
    parser.add_argument("csv_file", type=Path)
    parser.add_argument("--column", default=None, help="Numeric column to analyze. Defaults to score/value/last column.")
    return parser


def main(argv: list[str] | None = None) -> int:
    args = build_parser().parse_args(argv)
    try:
        values = read_numeric_values(args.csv_file, args.column)
        print(format_statistics(calculate_statistics(values)))
    except Exception as exc:
        print(f"Error: {{exc}}")
        return 2
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
'''


