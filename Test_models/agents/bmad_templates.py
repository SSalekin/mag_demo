def _file_organizer_app(spec: Any) -> str:
    request = _json(_request(spec))
    return f'''"""File organizer CLI generated by BMAD."""

from __future__ import annotations

import argparse
import shutil
from pathlib import Path

REQUEST = {request}


def extension_folder(path: Path) -> str:
    return path.suffix[1:].lower() if path.suffix else "no_extension"


def organize_by_extension(folder: Path) -> dict[str, int]:
    if not folder.exists() or not folder.is_dir():
        raise ValueError("Folder does not exist or is not a directory.")
    moved: dict[str, int] = {{}}
    for item in list(folder.iterdir()):
        if not item.is_file():
            continue
        name = extension_folder(item)
        target_dir = folder / name
        target_dir.mkdir(exist_ok=True)
        shutil.move(str(item), str(target_dir / item.name))
        moved[name] = moved.get(name, 0) + 1
    return moved


def format_result(result: dict[str, int]) -> str:
    if not result:
        return "No files moved."
    return "\\n".join(f"Moved {{count}} file(s) to .{{ext}}" for ext, count in sorted(result.items()))


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Organize files in a folder by extension.")
    parser.add_argument("folder", type=Path)
    return parser


def main(argv: list[str] | None = None) -> int:
    args = build_parser().parse_args(argv)
    try:
        print(format_result(organize_by_extension(args.folder)))
    except Exception as exc:
        print(f"Error: {{exc}}")
        return 2
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
'''


