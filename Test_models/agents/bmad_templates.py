def _chatbot_memory_app(spec: Any) -> str:
    request = _json(_request(spec))
    return f'''"""Simple chatbot with memory generated by BMAD."""

from __future__ import annotations

import argparse

REQUEST = {request}


def remember(memory: dict[str, str], key: str, value: str) -> str:
    memory[key.strip().lower()] = value.strip()
    return f"Remembered {{key.strip().lower()}}."


def reply(message: str, memory: dict[str, str]) -> str:
    text = message.strip()
    lower = text.lower()
    if lower.startswith("remember ") and " is " in lower:
        payload = text[len("remember "):]
        key, value = payload.split(" is ", 1)
        return remember(memory, key, value)
    if lower.startswith("what is "):
        key = lower[len("what is "):].strip(" ?")
        return memory.get(key, "I do not know yet.")
    return "I can remember facts. Try: remember name is Elwen"


def run_scripted_chat(messages: list[str]) -> list[str]:
    memory: dict[str, str] = {{}}
    return [reply(message, memory) for message in messages]


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Run a tiny scripted chatbot with memory.")
    parser.add_argument("messages", nargs="*", help="Messages to send. If empty, runs a demo.")
    return parser


def main(argv: list[str] | None = None) -> int:
    args = build_parser().parse_args(argv)
    messages = args.messages or ["remember name is Elwen", "what is name?"]
    for answer in run_scripted_chat(messages):
        print(answer)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
'''


